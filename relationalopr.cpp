#include<iostream>
using namespace std;

int main()
{
    int a,b;

    a=35;
    b=24;

    cout<<endl<<"Compare a>b "<<(a>b);

    cout<<endl<<"Compare a<b "<<(a<b);
    cout<<endl<<"Compare a=b "<<(a==b);
    cout<<endl<<"Compare a!=b "<<(a!=b);
    cout<<endl<<"Compare a>=b "<<(a>=b);
    cout<<endl<<"Compare a<=b "<<(a<=b);

    return 0;
}